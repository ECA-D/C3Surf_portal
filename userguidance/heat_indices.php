<h3>SU*</h3>
&#9679; <i>Summer days \((TX > 25&#176;C)\) (days)</i> 
<br><br> 

Let \(TX_{ij}\) be the daily maximum temperature at day \(i\) of period \(j\). Then counted is the no of days where:
<br><br>

\(TX_{ij} > 25&#176;C\)

<br><br>

<h3>CSU</h3>
